var nbre2 = prompt("Deuxième nombre ?");

if(nbre1 < nbre2){
     console.log(nbre1 + " est plus petit que "+nbre2);
}
else if(nbre1 > nbre2){
     console.log(nbre1 + " est plus grand que "+nbre2);
}
else {
     console.log(nbre1+ " et "+nbre2+" sont égaux.");
}
